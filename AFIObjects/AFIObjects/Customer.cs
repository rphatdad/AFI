using System;
using System.Collections.Generic;
using System.Text;

namespace AFIObjects
{
    public class Customer
    {
        // private members
        int iID;
        string strCustShortName;
        string strCustName;
        string strPhone1;
        string strPhone2;
        string strFax;
        string strEmail;
        string strContactName;
       
        // empty constructor
        public Customer()
        {
        }

        // full constructor
        public Customer(int ID, string CustShortName, string CustName, string Phone1, string Phone2, string Fax, string Email, string ContactName)
        {
            this.iID = ID;
            this.strCustShortName = CustShortName;
            this.strCustName = CustName;
            this.strPhone1 = Phone1;
            this.strPhone2 = Phone2;
            this.strFax = Fax;
            this.strEmail = Email;
            this.strContactName = ContactName;
           
        }
        public Customer(string CustShortName)
        {
            
            this.strCustShortName = CustShortName;
            this.strCustName = "";
            this.strPhone1 = "";
            this.strPhone2 = "";
            this.strFax = "";
            this.strEmail = "";
            this.strContactName = "";
            
        }

        // public accessors
        public int ID
        {
            get { return iID; }
            set { iID = value; }
        }
        public string CustShortName
        {
            get { return strCustShortName; }
            set { strCustShortName = value; }
        }
        public string CustName
        {
            get { return strCustName; }
            set { strCustName = value; }
        }
        public string Phone1
        {
            get { return strPhone1; }
            set { strPhone1 = value; }
        }
        public string Phone2
        {
            get { return strPhone2; }
            set { strPhone2 = value; }
        }
        public string Fax
        {
            get { return strFax; }
            set { strFax = value; }
        }
        public string Email
        {
            get { return strEmail; }
            set { strEmail = value; }
        }
        public string ContactName
        {
            get { return strContactName; }
            set { strContactName = value; }
        }


    }
}
